    private void getBalanceFromDatabase() {
   
    String sql = "SELECT notes FROM notes WHERE Id = ?";

    try (Connection con = DriverManager.getConnection(
             "jdbc:mysql://localhost:3306/tavrince_notes", "root", "123456");
         PreparedStatement pst = con.prepareStatement(sql)) {

        pst.setInt(1, Session.loggedInUserId);

        try (ResultSet rs = pst.executeQuery()) {
            if (rs.next()) {
                String notes = rs.getString("notes");
                notesField.setText(notes);
            } else {
                JOptionPane.showMessageDialog(null, "Notes not found");
            }
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Error fetching balance: " + e.getMessage());
    }
}
    
    public void makeEditorPaneTextBold(JEditorPane editorPane) {
    editorPane.setContentType("text/html");
    editorPane.setText("<html><b>" + editorPane.getText() + "</b></html>");
}
        public void undoBold(JEditorPane editorPane) {
    String text = editorPane.getText();

    // remove bold tags
    text = text.replace("<b>", "").replace("</b>", "");

    editorPane.setText(text);
}



    
     
    
