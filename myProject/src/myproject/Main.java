    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
     try (Connection con = DriverManager.getConnection(
        "jdbc:mysql://localhost:3306/tavrince_notes", "root", "123456")) {

    String newText = notesField.getText().trim();

    if (newText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Notes cannot be empty!");
        return;
    }
     
    // FIRST: GET EXISTING NOTES
    String selectSQL = "SELECT notes FROM notes WHERE Id = ?";
    PreparedStatement selectPst = con.prepareStatement(selectSQL);
    selectPst.setInt(1, Session.loggedInUserId);

    ResultSet rs = selectPst.executeQuery();
    String existingNotes = "";

    if (rs.next()) {
        existingNotes = rs.getString("notes");
    }

    rs.close();
    selectPst.close();

    // APPEND NEW NOTES TO OLD NOTES
    String updatedNotes = existingNotes + "\n" + newText;

    // UPDATE NOTES
    String updateSQL = "UPDATE notes SET notes = ? WHERE Id = ?";
    PreparedStatement updatePst = con.prepareStatement(updateSQL);
    updatePst.setString(1, updatedNotes);
    updatePst.setInt(2, Session.loggedInUserId);

    int rowUpdated = updatePst.executeUpdate();
    updatePst.close();

    if (rowUpdated > 0) {
        JOptionPane.showMessageDialog(this, "Your note has been added!");
        this.setVisible(false);
        new Main().setVisible(true);
    } else {
        JOptionPane.showMessageDialog(this, "No notes found for this account.");
    }

} catch (SQLException e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
}


        
    }//GEN-LAST:event_jButton1MouseClicked

