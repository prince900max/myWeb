    private void jButton4MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton4MouseClicked
        // TODO add your handling code here:
        if (product_Id.getText().isEmpty() || prodName.getText().isEmpty() || prodQuantity.getText().isEmpty() || prodPrice.getText().isEmpty() || prodCategory.getSelectedItem().equals("Select_Cataegory")) {
            JOptionPane.showMessageDialog(this, "Missing Information");
        } else {
            try{
                
                //declaration of string variable
                String productId = product_Id.getText();
                
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/supermarket_db","root","123456");
                
                String query = "UPDATE product SET product_Id = ?, name = ?, category = ?, quantity = ?, price = ? WHERE product_Id ='" + productId +"' ";
                
                PreparedStatement pst = con.prepareStatement(query);
                pst.setString(1, product_Id.getText());
                 pst.setString(2, prodName.getText());
                  pst.setString(3, prodCategory.getSelectedItem().toString());
                   pst.setString(4, prodQuantity.getText());
                    pst.setString(5, prodPrice.getText());
                    
                    int rowsUpdated = pst.executeUpdate();
                    if (rowsUpdated > 0) {
                        JOptionPane.showMessageDialog(this, "This product is updated");
                        
                        setVisisble(false);
                        new Products().setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(this, "product not found or updating failed");
                    }
                    pst.close();
                    con.close();
            }catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Invalid Product ID");
                
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton4MouseClicked

    private void jButton5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton5MouseClicked
        // TODO add your handling code here:
        if (product_Id.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Select the product to be deleted by clicking on the table row");   
        }else {
            try{
                Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/supermarket_db","root","123456");
                String productName = product_Id.getText();
                
                String query = "DELETE FROM product WHERE product_Id = ?";
                PreparedStatement stmt = con.prepareStatement(query);
                stmt.setString(1, productName);
                
                int rowsAffected = stmt.executeUpdate();
                
                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(null, "product deleted succesfully");
                    
                    setVisible(false);
                    new Products().setVisible(true);
                    
                } else {
                    JOptionPane.showMessageDialog(null, "No product found with that name");
                }
                
                stmt.close();
                con.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton5MouseClicked

