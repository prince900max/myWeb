    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        // TODO add your handling code here:
        if (product_Id.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Enter product_Id");
        }
        else if (prodQuantity.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Product Quantity is empty");
        }
        else if (prodName.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Product Name is empty");
        }
        else if (prodCategory.getSelectedItem().equals("Select_Category"))
        {
            JOptionPane.showMessageDialog(null, "Choose the category");
        }
        
        else {
            try{
                String query = "INSERT INTO `product`(`product_Id`, `name`, `category`, `quantity`, `price`) VALUES (?,?,?,?,?)";
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/supermarket_db","root","123456");
                pst = con.prepareStatement(query);
                pst.setString(1, product_Id.getText());
                pst.setString(2, prodName.getText());
                pst.setString(3, (String)prodCategory.getSelectedItem());
                pst.setString(4, prodQuantity.getText());
                pst.setString(5, prodPrice.getText());
                
                pst.executeUpdate();
                
                
                JOptionPane.showMessageDialog(null, "Product Added Successfully");
                
                setVisible(false);
                new Products().setVisible(true);
                       
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_jButton3MouseClicked

